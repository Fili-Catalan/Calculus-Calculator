/**
 * This is an enum class that defines a code for the various function types
 * encountered in Calculus.
 * 
 * There are four function types:
 *      - Sum
 *      - Quotient
 *      - Product
 *      - Composition
 */
public class FunctionTypes {

    enum FunctionType
    {
        SUM,
        QUOTIENT,
        PRODUCT,
        COMPOSITION
    }
    
}
